/**
 * @brief Запись адреса регистра для железного AY (чип 0).
 * Формирует сигналы BDIR=1, BC1=1, WR=1, A0=1 для фиксации адреса.
 * @param N_reg Номер регистра (0-15).
 */
void fast(ay_set_reg_hard)(uint8_t N_reg) 
{      
    N_sel_reg = N_reg;  // Только чип 0
    
    // Формируем посылку для фиксации адреса регистра чипа 0
    // Формат: *R*B10WA--------
    // R-reset, B-beep, 1-CS_1, 0-CS_0, W-WR, A-A0
    // Для адреса: CS0=1, CS1=0, WR=1, A0=1
    PIO_AY595->txf[SM_AY595] = (0b0100101100000000 | N_sel_reg | beep595) << 16;
    // Возвращаем в Z-состояние: CS0=1, CS1=0, WR=0, A0=0
    PIO_AY595->txf[SM_AY595] = (0b0100100000000000 | N_sel_reg | beep595) << 16;
}   

/**
 * @brief Запись данных в регистр железного AY (чип 0).
 * Формирует сигналы BDIR=1, BC1=0, WR=1, A0=0 для записи данных.
 * @param val Значение для записи в регистр.
 */
void fast(ay_set_data_hard)(uint8_t val)
{
    AY_set_reg(val);        // Сохраняем значение в программной копии регистров
    
    // Формируем посылку для записи данных в регистр чипа 0
    // Для данных: CS0=1, CS1=0, WR=1, A0=0
    PIO_AY595->txf[SM_AY595] = (0b0100101000000000 | val | beep595) << 16;
    // Возвращаем в Z-состояние
    PIO_AY595->txf[SM_AY595] = (0b0100100000000000 | val | beep595) << 16;
}

//==================================================================================================
// РАБОТА С ЖЕЛЕЗНЫМ TURBO SOUND (HARD_TS)
//==================================================================================================

/**
 * @brief Запись адреса регистра для железного Turbo Sound.
 * Поддерживает выбор чипа через специальные значения.
 * @param N_reg Номер регистра (0-15) или 0xFF (выбор чипа 0) / 0xFE (выбор чипа 1).
 */
void fast(ay_set_reg_hard_ts)(uint8_t N_reg) 
{   
    if (N_reg == 0xff) { chip_ay = 0; return; } 
    if (N_reg == 0xfe) { chip_ay = 1; return; }
    
    if (chip_ay == 0) { N_sel_reg = N_reg; }       // Выбран чип 0
    else { N_sel_reg_1 = N_reg; }                  // Выбран чип 1
    
    if (chip_ay == 0)  // Работа с чипом 0
    {
        // Фиксация адреса регистра чипа 0
        PIO_AY595->txf[SM_AY595] = (0b0100101100000000 | N_sel_reg | beep595) << 16;
        // Z-состояние чипа 0
        PIO_AY595->txf[SM_AY595] = (0b0100100000000000 | N_sel_reg | beep595) << 16;
    }
    else  // Работа с чипом 1
    {
        // Фиксация адреса регистра чипа 1
        // Для чипа 1: CS0=0, CS1=1, WR=1, A0=1
        PIO_AY595->txf[SM_AY595] = (0b0100011100000000 | N_sel_reg_1 | beep595) << 16;
        // Z-состояние чипа 1
        PIO_AY595->txf[SM_AY595] = (0b0100010000000000 | N_sel_reg_1 | beep595) << 16;
    }
}   

/**
 * @brief Запись данных в регистр железного Turbo Sound.
 * @param val Значение для записи в регистр.
 */
void fast(ay_set_data_hard_ts)(uint8_t val)
{ 
    if (chip_ay == 0)  // Запись в чип 0
    {
        AY_set_reg(val);
        // Запись данных в регистр чипа 0
        PIO_AY595->txf[SM_AY595] = (0b0100101000000000 | val | beep595) << 16;
        // Z-состояние чипа 0
        PIO_AY595->txf[SM_AY595] = (0b0100100000000000 | val | beep595) << 16;
    }
    else  // Запись в чип 1
    {
        AY_set_reg1(val);
        // Запись данных в регистр чипа 1
        // Для чипа 1: CS0=0, CS1=1, WR=1, A0=0
        PIO_AY595->txf[SM_AY595] = (0b0100011000000000 | val | beep595) << 16;
        // Z-состояние чипа 1
        PIO_AY595->txf[SM_AY595] = (0b0100010000000000 | val | beep595) << 16;
    }
}

