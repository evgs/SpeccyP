static uint16_t control_bits = 0;

#define LOW(x)  (control_bits &= ~(x))
#define HIGH(x) (control_bits |= (x))

#define AY_Enable  0b0100000000000000  // (1<<14) - Включение AY
#define CS_AY1     (1<<11)             // Chip Select чипа 1
#define CS_AY0     (1<<10)             // Chip Select чипа 0
#define Beeper     (1<<12)             // Бит бипера

/**
 * @brief Запись данных в чип SAA1099.
 * @param addr Адрес: 0 - данные, 1 - выбор регистра.
 * @param byte Значение для записи.
 */
void __not_in_flash_func(saa1099_write)(uint8_t addr, uint16_t byte)
{
    if (addr == 0)  // BC1 = 0 - запись данных
    {
        PIO_AY595->txf[SM_AY595] = (0b0000000000000000 | byte) << 16;
        busy_wait_us(5);
        PIO_AY595->txf[SM_AY595] = (0b0010000000000000 | byte) << 16;  // HIGH(CS_SAA1099)
    }
    
    if (addr == 1)  // BC1 = 1 - выбор регистра
    {
        PIO_AY595->txf[SM_AY595] = (0b0000000100000000 | byte) << 16;
        busy_wait_us(5);
        PIO_AY595->txf[SM_AY595] = (0b0010000100000000 | byte) << 16;  // HIGH(CS_SAA1099)
    }
}

