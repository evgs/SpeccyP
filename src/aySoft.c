/**
 * @brief Установка номера регистра для режима Turbo Sound (программная эмуляция).
 * Специальные значения:
 *   0xFF - выбрать чип 0
 *   0xFE - выбрать чип 1
 * @param N_reg Номер регистра или команда выбора чипа.
 */
void fast(ay_set_reg_soft_ts)(uint8_t N_reg) 
{
    if (N_reg == 0xff) { chip_ay = 0; return; } 
    if (N_reg == 0xfe) { chip_ay = 1; return; }
    if (chip_ay == 0) { N_sel_reg = N_reg; }      // Выбран чип 0
    else { N_sel_reg_1 = N_reg; }                  // Выбран чип 1
}
