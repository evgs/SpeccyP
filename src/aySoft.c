// Статические переменные для эмуляции чипа 0
static uint32_t noise_ay0_count = 0;        // Счетчик для генератора шума
static bool is_envelope_begin = false;      // Флаг начала нового цикла огибающей
static uint8_t ampl_ENV = 0;                // Текущая амплитуда огибающей (0-45)
static uint8_t envelope_ay_count = 0;       // Текущий шаг огибающей (0-31)

/**
 * @brief Чтение значения из регистра AY0.
 * @return Значение регистра.
 */
uint8_t fast(AY_get_reg)()
{
    return reg_ay0[N_sel_reg];
}

/**
 * @brief Запись значения в регистр AY0.
 * Применяет маску допустимых битов и обрабатывает специальные регистры.
 * @param val Значение для записи.
 */
void fast(AY_set_reg)(uint8_t val)
{
    if (N_sel_reg > 0x0f) return;  // Защита от выхода за границы (для TSFM)
    
    reg_ay0[N_sel_reg] = val & maskAY[N_sel_reg];
    
    switch (N_sel_reg)
    {
        case 6:  // R6 - частота генератора шума
            noise_ay0_count = 0;
            return;
        case 13: // R13 - выбор формы огибающей
            is_envelope_begin = true;  // Запускаем огибающую сначала
            return;
    }
}

/**
 * @brief Основной цикл генерации звука для AY0.
 * Вычисляет состояние всех трех каналов с учетом тонов, шума и огибающей.
 * @param delta Количество тиков таймера, прошедших с предыдущего вызова.
 * @return Указатель на массив из 3 значений uint16_t (уровни каналов A, B, C).
 */
uint16_t* fast(get_AY_Out)(uint8_t delta)
{   
    // Массив для хранения состояния битов каналов
    static bool bools[4];
    #define chA_bit (bools[0])   // Бит состояния тона канала A
    #define chB_bit (bools[1])   // Бит состояния тона канала B
    #define chC_bit (bools[2])   // Бит состояния тона канала C
    #define noise_bit (bools[3]) // Бит состояния генератора шума
  
    static uint32_t main_ay_count_env = 0;  // Счетчик для периода огибающей

    // Отдельные счётчики для каждого тонового генератора
    static uint32_t chA_count = 0;
    static uint32_t chB_count = 0;
    static uint32_t chC_count = 0;

    // Копирование прошлого значения каналов для более быстрой работы
    register bool chA_bitOut = chA_bit;
    register bool chB_bitOut = chB_bit;
    register bool chC_bitOut = chC_bit;

    // Вычисление текущих частот из регистров
    ay0_A_freq = ((reg_ay0[1] << 8) | reg_ay0[0]);
    ay0_B_freq = ((reg_ay0[3] << 8) | reg_ay0[2]);
    ay0_C_freq = ((reg_ay0[5] << 8) | reg_ay0[4]);
    ay0_R12_R11 = ((reg_ay0[12] << 8) | reg_ay0[11]);

    #define nR7 (~reg_ay0[7])  // Инвертированный R7 для прямой логики: 1 = Вкл, 0 = Выкл

    // Генерация тонов для каналов A, B, C
    if (nR7 & 0x1) {
        chA_count += delta;
        if (chA_count >= ay0_A_freq && (ay0_A_freq >= delta)) {
            chA_bit ^= 1;
            chA_count = 0;
        }
    } else {
        chA_bitOut = 1;
        chA_count = 0;
    }

    if (nR7 & 0x2) {
        chB_count += delta;
        if (chB_count >= ay0_B_freq && (ay0_B_freq >= delta)) {
            chB_bit ^= 1;
            chB_count = 0;
        }
    } else {
        chB_bitOut = 1;
        chB_count = 0;
    }

    if (nR7 & 0x4) {
        chC_count += delta;
        if (chC_count >= ay0_C_freq && (ay0_C_freq >= delta)) {
            chC_bit ^= 1;
            chC_count = 0;
        }
    } else {
        chC_bitOut = 1;
        chC_count = 0;
    }

    // Проверка запрещения тона в каналах (биты 0,1,2 регистра R7)
    // Если бит = 1, тон выключен (выход = 1)
    if (reg_ay0[7] & 0x1) chA_bitOut = 1;
    if (reg_ay0[7] & 0x2) chB_bitOut = 1;
    if (reg_ay0[7] & 0x4) chC_bitOut = 1;

    // Добавление шума, если он разрешен хотя бы в одном канале
    if (nR7 & 0x38)  // Проверка битов 3,4,5 (шум для A,B,C)
    {
        noise_ay0_count += delta;
        if (noise_ay0_count >= (reg_ay0[6] << 1)) {
            noise_bit = get_random();
            noise_ay0_count = 0;
        }
        
        // Если бит шума == 0, он "притягивает" выход канала к земле
        if (!noise_bit)
        {            
            if ((chA_bitOut) && (nR7 & 0x08)) chA_bitOut = 0;  // Шум в канале A
            if ((chB_bitOut) && (nR7 & 0x10)) chB_bitOut = 0;  // Шум в канале B
            if ((chC_bitOut) && (nR7 & 0x20)) chC_bitOut = 0;  // Шум в канале C
        }
    }

    // Вычисление амплитуды огибающей
    // Проверяем, используется ли огибающая хотя бы в одном канале (бит 4 в R8,R9,R10)
    if ((reg_ay0[8] & 0x10) | (reg_ay0[9] & 0x10) | (reg_ay0[10] & 0x10))
    {   
        main_ay_count_env += delta;
        
        // Сброс огибающей при записи в R13
        if (is_envelope_begin)
        {
            envelope_ay_count = 0;
            main_ay_count_env = 0;
            is_envelope_begin = false;
        }

        // Проверка периода огибающей
        if ((main_ay_count_env) >= (ay0_R12_R11 << 1))
        {
            main_ay_count_env -= ay0_R12_R11 << 1;
            
            // Зацикливание огибающей для определенных форм
            if (envelope_ay_count >= 32)
            {
                // Формы 8,10,12,14 (0x08,0x0A,0x0C,0x0E) зацикливаются
                if ((reg_ay0[13] & 0x08) && ((~reg_ay0[13]) & 0x01))
                    envelope_ay_count = 0;  // Зацикливание
                else
                    envelope_ay_count = 31; // Удержание последнего значения
            }

            // Получение значения из таблицы огибающих и преобразование через таблицу громкости
            uint8_t x = Envelopes_const[reg_ay0[13]][envelope_ay_count];
            ampl_ENV = ampls_AY_table[x];
            envelope_ay_count++;
        }
    }

    // Массив для выходных значений каналов
    static uint16_t outs[3];
    #define outA (outs[0])
    #define outB (outs[1])
    #define outC (outs[2])
              
    // Вычисление финальной амплитуды для каждого канала
    // Если бит 4 регистра амплитуды = 1, используется огибающая, иначе фиксированная громкость
    outA = chA_bitOut ? ((reg_ay0[8] & 0xf0) ? ampl_ENV : ampls_AY_table[reg_ay0[8]]) : 0;
    outB = chB_bitOut ? ((reg_ay0[9] & 0xf0) ? ampl_ENV : ampls_AY_table[reg_ay0[9]]) >> 1 : 0;
    outC = chC_bitOut ? ((reg_ay0[10] & 0xf0) ? ampl_ENV : ampls_AY_table[reg_ay0[10]]) : 0;
      
    return outs;
}
