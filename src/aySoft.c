/**
 * @brief Сброс всех звуковых чипов в начальное состояние.
 * Обнуляет регистры, сбрасывает огибающие, инициализирует порты ввода/вывода.
 */
void AY_reset(void)
{
    switch (conf.type_sound)
    {
        case SOFT_AY:
        case SOFT_TS:
        case I2S_AY:
        case I2S_TS:
            // Сброс программных регистров
            memset(reg_ay0, 0, 16);
            reg_ay0[14] = 0xff;  // Порт A ввода/вывода - все биты на ввод (pull-up)
            memset(reg_ay1, 0, 16);
            reg_ay1[14] = 0xff;  // Порт A второго чипа
            break;
            
        case HARD_AY:
        case HARD_TS:
            pwm_set_gpio_level(CLK_AY_PIN, 2);  // Включение тактовой частоты AY
            send595(AY_RES, 0x00);              // Аппаратный сброс AY
            send595(AY_Z, 0x00);                // Перевод в Z-состояние
            break;
            
        case TSFM:
            pwm_set_gpio_level(CLK_AY_PIN, 2);  // Включение тактовой частоты
            send595(AY_RES, 0x00);              // Сброс
            send595(AY_Z, 0x00);                // Z-состояние
            break;
            
        case TS_SAA:
            pwm_set_gpio_level(CLK_AY_PIN, 2);  // Включение тактовой частоты
            send595(AY_RES, 0x00);              // Сброс AY
            send595(AY_Z, 0x00);                // Z-состояние
            // Инициализация SAA1099 (запись нулей во все регистры)
            for (int i = 0; i < 0x20; i++) {
                saa1099_write(1, i);            // Выбор регистра
                saa1099_write(0, 0x00);         // Запись нулевого значения
            }
            break;
            
        default:
            break;
    }
}

