uint16_t noise_fdd = 0;      // Значение шума FDD для PWM вывода
uint16_t noise_fddI2S = 0;   // Значение шума FDD для I2S вывода (с учетом громкости)

/**
 * @brief Генерация звука перемещения головок дисковода (FDD).
 * Использует данные из буфера дорожки для создания характерного шума.
 */
void sound_fdd(void)
{
    if (!vbuf_en) return;           // Буфер дорожки не активен
    if (conf.sound_fdd) return;     // Звук FDD отключен в настройках
    
    static uint8_t old_sound_track = 0;
    static uint16_t st = 32768;     // Индекс в буфере дорожки
    
    // Сброс при смене трека
    if (sound_track != old_sound_track)
    {
        if (st == 32768) st = 0;
        old_sound_track = sound_track;
    }
    
    st++;
    if (st < 32000)
    {
        // Извлечение шума из данных дорожки (младшие 5 бит)
        noise_fdd = (g_gbuf[st + 1]) & 0x1f;
        noise_fddI2S = noise_fdd * conf.vol_i2s;
    }
    else
    {
        // Конец буфера - обнуление
        noise_fdd = 0;
        noise_fddI2S = 0;
        st = 32768;
    }
}

