// Начальное значение (seed) для LFSR
uint32_t __not_in_flash_func(noise_state) = 0xACE1u;

/**
 * @brief Генерация одного бита псевдослучайного шума.
 * Используется 32-битный LFSR с периодом около 4 миллиардов.
 * @return true если старший бит = 1, иначе false.
 */
bool __not_in_flash_func(get_random)()
{
    // Качественный 32-битный LFSR (Linear Feedback Shift Register)
    // Полином: x^32 + x^30 + x^29 + x^1 + 1 (0xD0000001)
    noise_state = (noise_state >> 1) ^ (-(noise_state & 1u) & 0xD0000001u);
    return noise_state & 0x80000000;  // Возвращаем старший бит для лучшей случайности
}

