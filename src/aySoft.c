// РАБОТА С TSFM (Turbo Sound FM) - ЗАГОТОВКА
//==================================================================================================

// Биты управления для TSFM:
// 1<<0 - выбор чипа модуля TS
// 1<<1 - статус (0 - чтение статуса, 1 - нет чтения статуса)
// 1<<2 - включение/выключение FM
// 1<<3 - зарезервировано под SAA1099
uint8_t TSFM_MaskReadyBit = 0xff;  // 0x7F = NotReady, 0xFF = Ready

/**
 * @brief Запись номера регистра для TSFM.
 * @param N_reg Номер регистра или команда управления.
 */
void fast(ay_set_reg_tsfm)(uint8_t N_reg) 
{ 
    if (N_reg >= 0xf0) {
        if ((N_reg & 0x01) == 1) { chip_ay = 0; }      // Выбор чипа 0 (0xFF)
        if ((N_reg & 0x01) == 0) { chip_ay = 1; }      // Выбор чипа 1 (0xFE)
        if ((N_reg & 0x02) == 0) { TSFM_MaskReadyBit = 0x7f; } 
        else { TSFM_MaskReadyBit = 0xff; }             // Управление чтением статуса
        return;
    }

    if (chip_ay == 0)  // Чип 0
    {
        N_sel_reg = N_reg;
        // WR=0, A0=0, CS=0 - подготовка к записи адреса
        PIO_AY595->txf[SM_AY595] = (0b0100100000000000 | N_sel_reg | beep595) << 16;
        // WR=1, CS=1, A0=0 - фиксация (запись при переходе)
        PIO_AY595->txf[SM_AY595] = (0b0100111000000000 | N_sel_reg | beep595) << 16;
    }
    else  // Чип 1
    {
        N_sel_reg_1 = N_reg;
        // Подготовка к записи адреса для чипа 1
        PIO_AY595->txf[SM_AY595] = (0b0100010000000000 | N_sel_reg_1 | beep595) << 16;
        // Фиксация адреса
        PIO_AY595->txf[SM_AY595] = (0b0100111000000000 | N_sel_reg_1 | beep595) << 16;
    }
}  

/**
 * @brief Запись данных в регистр TSFM.
 * @param val Значение для записи.
 */
void fast(ay_set_data_tsfm)(uint8_t val)
{
    if (chip_ay == 0)  // Чип 0
    { 
        AY_set_reg(val & TSFM_MaskReadyBit);  // Сохраняем с учетом маски готовности
        
        // WR=0, A0=1, CS=0 - подготовка к записи данных
        PIO_AY595->txf[SM_AY595] = (0b0100100100000000 | val | beep595) << 16;
        // WR=1, CS=1, A0=1 - фиксация данных
        PIO_AY595->txf[SM_AY595] = (0b0100111100000000 | val | beep595) << 16;
    }
    else  // Чип 1
    {
        AY_set_reg1(val & TSFM_MaskReadyBit);  // Сохраняем с учетом маски готовности
        
        // Подготовка к записи данных для чипа 1
        PIO_AY595->txf[SM_AY595] = (0b0100010100000000 | val | beep595) << 16;
        // Фиксация данных
        PIO_AY595->txf[SM_AY595] = (0b0100111100000000 | val | beep595) << 16;
    }
    busy_wait_us(12);  // Задержка для стабильности
}

