/**
 * @brief Основной цикл генерации звука для AY1.
 * Вычисляет состояние всех трех каналов с учетом тонов, шума и огибающей.
 * @param delta Количество тиков таймера, прошедших с предыдущего вызова.
 * @return Указатель на массив из 3 значений uint16_t (уровни каналов A, B, C).
 */
uint16_t *fast(get_AY_Out1)(uint8_t delta)
{
    // Массив для хранения состояния битов каналов
    static bool bools1[4];
    #define chA_bit_1 (bools1[0])   // Бит состояния тона канала A
    #define chB_bit_1 (bools1[1])   // Бит состояния тона канала B
    #define chC_bit_1 (bools1[2])   // Бит состояния тона канала C
    #define noise_bit_1 (bools1[3]) // Бит состояния генератора шума
  
    static uint32_t main_ay1_count_env = 0;  // Счетчик для периода огибающей

    // Отдельные счётчики для каждого тонового генератора
    static uint32_t chA1_count = 0;
    static uint32_t chB1_count = 0;
    static uint32_t chC1_count = 0;

    // Копирование прошлого значения каналов для более быстрой работы
    register bool chA_bit_1Out = chA_bit_1;
    register bool chB_bit_1Out = chB_bit_1;
    register bool chC_bit_1Out = chC_bit_1;
    
    // Вычисление текущих частот из регистров
    ay1_A_freq = ((reg_ay1[1] << 8) | reg_ay1[0]);
    ay1_B_freq = ((reg_ay1[3] << 8) | reg_ay1[2]);
    ay1_C_freq = ((reg_ay1[5] << 8) | reg_ay1[4]);
    ay1_R12_R11 = ((reg_ay1[12] << 8) | reg_ay1[11]);

    #define n1R7 (~reg_ay1[7])  // Инвертированный R7 для прямой логики: 1 = Вкл, 0 = Выкл

    // Генерация тонов для каналов A, B, C
    if (n1R7 & 0x1) {
        chA1_count += delta;
        if (chA1_count >= ay1_A_freq && (ay1_A_freq >= delta)) {
            chA_bit_1 ^= 1;
            chA1_count = 0;
        }
    } else {
        chA_bit_1Out = 1;
        chA1_count = 0;
    }

    if (n1R7 & 0x2) {
        chB1_count += delta;
        if (chB1_count >= ay1_B_freq && (ay1_B_freq >= delta)) {
            chB_bit_1 ^= 1;
            chB1_count = 0;
        }
    } else {
        chB_bit_1Out = 1;
        chB1_count = 0;
    }

    if (n1R7 & 0x4) {
        chC1_count += delta;
        if (chC1_count >= ay1_C_freq && (ay1_C_freq >= delta)) {
            chC_bit_1 ^= 1;
            chC1_count = 0;
        }
    } else {
        chC_bit_1Out = 1;
        chC1_count = 0;
    }

    // Проверка запрещения тона в каналах
    if (reg_ay1[7] & 0x1) chA_bit_1Out = 1;
    if (reg_ay1[7] & 0x2) chB_bit_1Out = 1;
    if (reg_ay1[7] & 0x4) chC_bit_1Out = 1;

    // Добавление шума, если он разрешен хотя бы в одном канале
    if (n1R7 & 0x38)  // Проверка битов 3,4,5 (шум для A,B,C)
    {
        noise_ay1_count += delta;
        if (noise_ay1_count >= (reg_ay1[6] << 1)) {
            noise_bit_1 = get_random();
            noise_ay1_count = 0;
        }
        
        if (!noise_bit_1)
        {            
            if ((chA_bit_1Out) && (n1R7 & 0x08)) chA_bit_1Out = 0;  // Шум в канале A
            if ((chB_bit_1Out) && (n1R7 & 0x10)) chB_bit_1Out = 0;  // Шум в канале B
            if ((chC_bit_1Out) && (n1R7 & 0x20)) chC_bit_1Out = 0;  // Шум в канале C
        }
    }

    // Вычисление амплитуды огибающей
    if ((reg_ay1[8] & 0x10) | (reg_ay1[9] & 0x10) | (reg_ay1[10] & 0x10))
    {   
        main_ay1_count_env += delta;
        
        if (is_envelope_begin1)
        {
            envelope_ay1_count = 0;
            main_ay1_count_env = 0;
            is_envelope_begin1 = false;
        }

        if ((main_ay1_count_env) >= (ay1_R12_R11 << 1))
        {
            main_ay1_count_env -= ay1_R12_R11 << 1;
            
            if (envelope_ay1_count >= 32)
            {
                if ((reg_ay1[13] & 0x08) && ((~reg_ay1[13]) & 0x01))
                    envelope_ay1_count = 0;  // Зацикливание
                else
                    envelope_ay1_count = 31; // Удержание последнего значения
            }
            
            uint8_t x = Envelopes_const[reg_ay1[13]][envelope_ay1_count];
            ampl_ENV_1 = ampls_AY_table[x];
            envelope_ay1_count++;
        }
    }

    // Массив для выходных значений каналов
    static uint16_t outs1[3];
    #define outA1 (outs1[0])
    #define outB1 (outs1[1])
    #define outC1 (outs1[2])
          
    // Вычисление финальной амплитуды для каждого канала
    outA1 = chA_bit_1Out ? ((reg_ay1[8] & 0xf0) ? ampl_ENV_1 : ampls_AY_table[reg_ay1[8]]) : 0;
    outB1 = chB_bit_1Out ? ((reg_ay1[9] & 0xf0) ? ampl_ENV_1 : ampls_AY_table[reg_ay1[9]]) >> 1 : 0;
    outC1 = chC_bit_1Out ? ((reg_ay1[10] & 0xf0) ? ampl_ENV_1 : ampls_AY_table[reg_ay1[10]]) : 0;
    
    return outs1;
}

