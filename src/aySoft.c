/**
 * @brief Установка громкости I2S на внешнем сопроцессоре General Sound.
 * Отправляет команду SERVICE_COMMAND с параметром TS_VOLUME по шине picobus.
 */
void init_vol_ay(void)
{
    while (picobus_busy) { busy_wait_us(100); } // Ожидание освобождения шины picobus
    PBUS_CS_0;                                   // Активация выбора устройства (Chip Select = 0)
    if (conf.vol_i2s > 100) conf.vol_i2s = 100;  // Ограничение громкости до 100%
    const uint8_t init_msg[] = { SERVICE_COMMAND, TS_VOLUME, conf.vol_i2s }; 
    send_buffer(init_msg, sizeof(init_msg));     // Отправка буфера команды
    PBUS_CS_1;                                   // Деактивация выбора устройства (Chip Select = 1)
}

/**
 * @brief Установка режима усилителя (Audio Booster) на General Sound.
 * Отправляет команду SERVICE_COMMAND с параметром TS_BUSTER.
 */
void set_audio_buster(void)
{
    while (picobus_busy) { busy_wait_us(100); }  // Ожидание освобождения шины picobus
    PBUS_CS_0;
    const uint8_t init_msg[] = { SERVICE_COMMAND, TS_BUSTER, conf.audio_buster }; 
    send_buffer(init_msg, sizeof(init_msg));
    PBUS_CS_1;
}

/**
 * @brief Обработчик вывода бипера для режима General Sound.
 * @param val Состояние бита бипера (true - высокий уровень, false - низкий).
 */
void __not_in_flash_func(gsp_beep_out)(bool val)
{
    static bool out;
    beep_data = (beep_data & 0b10) | (val << 0);    // Обновляем бит 0 (beep), сохраняя бит 1 (tape)
    out ^= (beep_data == beep_data_old) ? 0 : 1;     // Изменяем выход только при изменении данных
    beep_data_old = beep_data;
    gpio_put(BEEP_PIN, val);                         // Выводим сигнал на пин бипера
}

/**
 * @brief Инициализация звуковой подсистемы при работе с General Sound.
 * Устанавливает указатели на функции и отправляет начальные настройки громкости.
 */
void select_audio(void)
{
    conf.type_sound = I2S_AY;           // Принудительно устанавливаем режим I2S_AY
    hw_beep_out = gsp_beep_out;         // Назначаем обработчик бипера
    init_vol_ay();                      // Установка громкости AY
    set_audio_buster();                 // Установка усилителя I2S
}

